package com.aspire.dotcard.awms;

public class CategoryMappingVO {
	private String awmsCategoryId;
	private String awmsPCategoryId;
	private String categoryId;
	private String name;
	public String getAwmsCategoryId() {
		return awmsCategoryId;
	}
	public void setAwmsCategoryId(String awmsCategoryId) {
		this.awmsCategoryId = awmsCategoryId;
	}
	public String getAwmsPCategoryId() {
		return awmsPCategoryId;
	}
	public void setAwmsPCategoryId(String awmsPCategoryId) {
		this.awmsPCategoryId = awmsPCategoryId;
	}
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
